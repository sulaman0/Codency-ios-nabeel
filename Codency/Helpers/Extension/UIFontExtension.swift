import UIKit

extension UIFont{
    public static var GothamRegular: UIFont?{
        return UIFont(name: "Gotham-Book", size: 10)
    }
    
    public static var GothamBold: UIFont?{
        return UIFont(name: "Gotham-Bold", size: 10)
    }
    
    public static var GothamMedium: UIFont?{
        return UIFont(name: "Gotham-Medium", size: 10)
    }
    
    public static var GothamLight: UIFont?{
        return UIFont(name: "Gotham-Light", size: 10)
    }
}
